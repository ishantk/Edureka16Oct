package co.edureka;

public class Certificate {

	// Attributes
	int cid;
	String name;
	
	//Methods
	public int getCid() {
		return cid;
	}
	public void setCid(int cid) {
		this.cid = cid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
